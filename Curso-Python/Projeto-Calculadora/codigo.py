def soma(a, b):
    return a + b 

def sobtracao(a, b):
    return a - b 

def multiplicacao(a, b):
    return a * b

def divisao(a, b):
    return a / b 


valor_1 = 10
valor_2 = 20

print(f"valor 1 + valor 2: {soma(valor_1, valor_2)}")
print(f"valor 1 - valor 2: {sobtracao(valor_1, valor_2)}")
print(f"valor 1 * valor 2: {multiplicacao(valor_1, valor_2)}")
print(f"valor 1 / valor 2: {divisao(valor_1, valor_2)}")